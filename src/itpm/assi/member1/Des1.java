    private boolean ValidateFields(){
        if(AYS.getText().equalsIgnoreCase("") || Gno.getText().equalsIgnoreCase("") || SGno.getText().equalsIgnoreCase("") || prg.getSelectedItem().toString().equalsIgnoreCase("Programme")){
            JOptionPane.showMessageDialog(null, "Please insert all fields.");
            return false;
        }
        else{
                return true; 
        }   
    }
    
    private void savestuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savestuActionPerformed
                
        if(!ValidateFields()){
            return;
        }

        
        try{
            conn = connection.getConnection();
                
                String sql = "insert into student" 
                        +"(academic_year_sem,programme,group_no,subgroup_no,group_id,subgroup_id)"
                        +"values(?,?,?,?,?,?)";
                               
                PreparedStatement pstmt = conn.prepareStatement(sql);
                
                pstmt.setString(1,AYS.getText());
                
                String programme;
                programme = prg.getSelectedItem().toString();
                pstmt.setString(2, programme);
                
                pstmt.setInt(3,Integer.parseInt(Gno.getText()));
               
                pstmt.setInt(4,Integer.parseInt(SGno.getText()));
                
                pstmt.setString(5,Gid.getText());;
                pstmt.setString(6,SGid.getText());
                
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Insertion Success");
                conn.close();
           
            
        }catch(Exception e)
        {
                JOptionPane.showMessageDialog(null,e);
                System.out.println("not inserted");
        }
// TODO add your handling code here:
        
    }//GEN-LAST:event_savestuActionPerformed

