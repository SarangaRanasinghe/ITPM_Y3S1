    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         // TODO add your handling code here:       
        int i =  jTable1.getSelectedRow();
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        String selected_day = tblModel.getValueAt(i,1).toString();
            switch(selected_day){
                case "Monday":
                    dayselectbtn2.setSelectedIndex(0); 
                    break;
                case "Tuesday":
                    dayselectbtn2.setSelectedIndex(1); 
                    break;
                case "Wednesday":
                    dayselectbtn2.setSelectedIndex(2); 
                    break;
                case "Thursday":
                    dayselectbtn2.setSelectedIndex(3); 
                    break;
                case "Friday":
                    dayselectbtn2.setSelectedIndex(4); 
                    break;
                case "Sturday":
                    dayselectbtn2.setSelectedIndex(5); 
                    break;
                case "Sunday":
                    dayselectbtn2.setSelectedIndex(6); 
                    break;
            }
        
        String time_slot = tblModel.getValueAt(i,2).toString();
           if(time_slot.equals("hrbtn2")){
             hrbtn2.setSelected(true);
           }
           else{
              minbtn2.setSelected(true);
           }
         
        stime3.setText(tblModel.getValueAt(i, 3).toString());
        
    }//GEN-LAST:event_jTable1MouseClicked

