    private void deltagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deltagActionPerformed
            try{
                conn = connection.getConnection();
                
                String sql = "delete from tag where id = ?";
            
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setInt(1,Integer.parseInt(ID.getText()));
               
                int result = pstmt.executeUpdate();
                if( result > 0 ){
         
                        JOptionPane.showMessageDialog(null, "Record Deleted Successfully!!!");
                        ClearFields();
                    }
                    else{
                            JOptionPane.showMessageDialog(null, "Record Could Not Be Found");
          
                
                DisplayTable();
                conn.close();
                
                }

             
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
            }
            
            


        // TODO add your handling code here:
    }//GEN-LAST:event_deltagActionPerformed

    private void mngTagsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mngTagsMouseClicked
      ID.setText( mngTags.getValueAt(mngTags.getSelectedRow(), 0).toString());
        tagName.setText( mngTags.getValueAt(mngTags.getSelectedRow(), 1).toString());
        tagCode.setText( mngTags.getValueAt(mngTags.getSelectedRow(), 2).toString());
        
       
       String comboVal=mngTags.getValueAt(mngTags.getSelectedRow(), 3).toString();
        if(comboVal.equalsIgnoreCase("lecture")){
            relTag.setSelectedIndex(1);
        } else if (comboVal.equalsIgnoreCase("tutorial")) {
            relTag.setSelectedIndex(2);
        } else if (comboVal.equalsIgnoreCase("lab")) {
            relTag.setSelectedIndex(3);
        } else {
            relTag.setSelectedIndex(0);
        }
    
        



// TODO add your handling code here:
        
    }//GEN-LAST:event_mngTagsMouseClicked

