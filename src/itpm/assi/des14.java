         private void DisplayTable(){
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/itpmproject?zeroDateTimeBehavior=convertToNull", "root",""); 
            
            String sql = "select * from time_slot"; 
            stmt = con.prepareStatement(sql);
            rs = stmt.executeQuery(sql);
            
            while(rs.next()){
                
                 String id = String.valueOf(rs.getInt("id"));
                 String selected_day = rs.getString("selected_day");
                 String time_slot = rs.getString("time_slot");
                 String starting_time = rs.getString("starting_time");
                 
                 String tbData[] = {id,selected_day,time_slot,starting_time};
                 DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel(); 
                 
                 tblModel.addRow(tbData);
                
            }
      
        }catch(Exception e){
            
           JOptionPane.showMessageDialog(null, e); 
        
        }
          
    }

